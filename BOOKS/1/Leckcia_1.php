				Рис.1. Структура экономической информации

     Реквизиты – элементарные неделимые единицы ЭИ, выражающие определённые свойства объекта.  Реквизит (синоним этого понятия - поле, атрибут, элементарная единица информации).
Реквизиты делятся на реквизиты – основания и реквизиты - признаки. Реквизиты- признаки характеризуют качественные признаки (свойства) описываемого объекта  (время и место действия, действующих лиц, ФИО, предметы и продукты труда, наименование работы).
Реквизиты- основания дают количественную характеристику явлений, объектов, выраженную в определённых единицах измерениях (количество, стоимость, норму времени и т.д.). Отдельно взятые реквизиты-признаки и реквизиты-основания экономического смысла не имеют и используются только в сочетании друг с другом.
	При обработке ЭИ  над реквизитами- основаниями выполняют арифметические операции, а с помощью реквизитов- признаков осуществляют поиск информации, сортировку, выборку,  логические операции.
Однородные по форме реквизиты-признаки, которые имеют разные значения, объединяют в номенклатуры (номенклатура продукции, материалов и т.д.).
Совокупность логически связанных реквизитов-признаков и реквизитов-оснований, имеющая экономический смысл, образует показатель. Можно сказать и по-другому:  показатель – это минимальная по информационному содержанию единица информации, но достаточная для образования документа. Пример показателей – себестоимость, фондоотдача,  фондоёмкость,  производительность труда, рентабельность и т.д.  На основании показателей строятся документы. Документы, используемые в процессе управления, планирования и учёта, могут включать один или несколько показателей.
	Совокупность информации. Достаточной для выработки суждения (заключения) про конкретный процесс, называется информационным сообщением. 
Информационный массив –практически интерпретирует номенклатуру (объединяет реальные значения реквизитов, которые образуют информационное сообщение).
Под массивами понимается структурная ЕИ, представляющая набор данных одной формы со всеми их значениями, либо сочетание таких наборов данных, относящихся к одной задаче подсистемы. В последнем случае массив называется укрупненным.
Примером массива служит совокупность данных об учете материальных ценностей на складах предприятия, а примером укрупненного – движение этих материальных ценностей. Массив, в свою очередь, состоит из более мелких информационных единиц, обычно из показателей, в некоторых случаях только из реквизитов одного вида. 
	Информационный поток – совокупность инф. массивов, которые относятся к одному участку управленческой работы.
	Информационная подсистема – совокупность информационных потоков, которые характеризуют в целом одну  управленческую функцию. 
