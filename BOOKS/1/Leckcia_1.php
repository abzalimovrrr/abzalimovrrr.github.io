                <p2>Понятие информационная потребность тесно связано с понятием цели и функции управления. Можно сказать, что потребность в информации - это условия, по которым определённые данные содействуют достижению целей управления.
 Информация, которая обслуживает процессы производства, распределения, обмена и потребления  материальных благ и обеспечивает решение задач организационно – экономического  управления называется управленческой.
 Она представляет собой разнообразные сведения  экономического, технологического, социального, юридического, демографического и др. содержания. Таким образом, информация является одним из важнейших ресурсов наряду с энергетическими, материальными, трудовыми и финансовыми ресурсами.
Экономическая  информация (ЭИ) и потребность в ней возникает в процессе экономической деятельности людей.  ЭИ – это информация, характеризующая процессы и явления производственно-хозяйственной деятельности объектов управления и их структурных звеньев.
ЭИ является предметом автоматизированной обработки и:
Имеет тенденцию к постоянному увеличению объемов данных;
Отображает  все стороны деятельности предприятий, организаций через систему натуральных, стоимостных и других показателей;
Может быть цифровой, символьной, имеет линейную форму подачи;
В основной своей массе дискретна и фиксируется на материальных носителях;
Характеризуется массовостью, большими объемами, долговременным сохранением и небходимостью накопления, повторением циклов возникновения и обработки в установленных  временных границах;
       Характиризуется многообразием  источников и потребителей;
       Характиризуется значительным удельным весом логических операций при её обработке;
 Имеет сложную структуру.
ЭИ должна также обладать следующими свойствами:
  1. достоверность и полнота;
  2. ценность и актуальность;
  3. ясность и понятность.
Информация достоверна, если она не искажает истинное положение дел.  Информация полна, если её достаточно для понимания и принятия решений. Ценность  информации зависит от того, какие задачи решаются с её помощью. Ясность и понятность - заключаются в том, что информация описана  языком пользователя 
ЭИ в процессе обработки   преобразуется в новые знания, которые могут использоваться для принятия управленческих   решений и пополнения знаний управленческого персонала.
Составляющими информационной системы (базы)  предприятия является:
-	конструкторско-технологическая документация (она характеризует структуру и технологию изготовления изделий);
-	нормативно-справочная и расценочная информация;
-	учётная (фактическая) информация. С её помощью определяется степень выполнения плана производства по всем параметрам и соответствие намеченным  технологическим признакам.
 ЭИ отражает деятельность предприятий и организаций посредством натуральных, стоимостных и других показателей.  
Структура экономической информации определяется ее строением, выделением некоторых элементов, которые называются информационными единицами. Такие элементы образуют сложные составные иерархические уровни структурного строения информации чаще всего между инф. единицами устанавливается иерархическая зависимость: от простых к сложным сверху вниз. 
Структура ЭИ достаточно сложна и может включать различные комбинации информационных совокупностей.
    Информационная совокупность – это группа данных, характеризующих объект, процесс, операцию.
 По структурному содержанию информационные совокупности можно разделить на: Реквизиты, Показатели, Информационное сообщение, Информационный Массив, Информационный поток, Информационная подсистема, Информационная система.
				
