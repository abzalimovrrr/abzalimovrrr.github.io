<h3>Инструкции</h3>
<p>Базовым строительным блоком программы являются <span class="b">инструкции</span> (statement). Инструкция представляет некоторое 
действие, например, арифметическую операцию, вызов метода, объявление переменной и присвоение ей значения. В конце каждой инструкции 
в C# ставится точка с запятой (;). Данный знак указывает компилятору на конец инструкции. Например:</p>
<pre class="brush:c#;">Console.WriteLine("Привет");</pre>
<p>Данная строка представляет вызов метода <code>Console.WriteLine</code>, который выводит на консоль строку. В данном случае вызов метода является 
инструкцией и поэтому завершается точкой с запятой.</p>
<p>Набор инструкций может объединяться в блок кода. Блок кода заключается в фигурные скобки, а инструкции помещаются между открывающей и 
закрывающей фигурными скобками:</p>
<pre class="brush:c#;">
{
    Console.WriteLine("Привет");
    Console.WriteLine("Добро пожаловать в C#");
}
</pre>
<p>В данном блоке кода две инструкции, которые выводят на консоль определенную строку.</p>
<p>Одни блоки кода могут содержать другие блоки:</p>
<pre class="brush:c#;">
{
	Console.WriteLine("Первый блок");
	{
		Console.WriteLine("Второй блок");
	}
}
</pre>
<h3>Метод Main</h3>
<p>Точкой входа в программу на языке C# является метод Main. При создании проекта консольного приложения в Visual Studio, например, создается следующий 
метод Main:</p>
<pre class="brush:c#;">
class Program
{
	static void Main(string[] args)
	{
		// здесь помещаются выполняемые инструкции
	}
}
</pre>
<p>По умолчанию метод Main размещается в классе Program. Название класса может быть любым. Но метод Main является обязательной частью консольного приложения. 
Если мы изменим его название, то программа не скомпилируется.</p>
<p>По сути и класс, и метод представляют своего рода блок кода: блок метода помещается в блок класса. Внутри блока метода Main располагаются выполняемые 
в программе инструкции.</p>
<h3>Регистрозависимость</h3>
<p>C# является регистрозависимым языком. Это значит, в зависимости от регистра символов какое-то определенные названия может представлять разные классы, методы, 
переменные и т.д. Например, название обязательного метода Main начинается именно с большой буквы: "Main". Если мы назовем метод "main", то программа 
не скомпилируется, так как метод, который представляет стартовую точку в приложении, обязательно должен называться "Main", а не "main" или "MAIN".</p>
<h3>Комментарии</h3>
<p>Важной частью программного кода являются комментарии. Они не являются собственно частью программы, при компиляции они игнорируются. 
Тем не менее комментарии делают код программы более понятным, помогая понять те или иные его части.</p>
<p> есть два типа комментариев: однострочный и многострочный. Однострочный комментарий размещается на одной строке после двойного слеша 
//. А многострочный комментарий заключается между символами /* текст комментария */. Он может размещаться на нескольких строках. 
Например:</p>
<pre class="brush:c#;">
using System;

namespace HelloApp
{
	/*
		программа, которая спрашивает у пользователя имя
		и выводит его на консоль
	*/
    class Program
    {
		// метод Main - стартовая точка приложения
        static void Main(string[] args)
        {
            Console.Write("Введите свое имя: ");
            string name = Console.ReadLine();		// вводим имя
            Console.WriteLine($"Привет {name}");	// выводим имя на консоль	
			Console.ReadKey();
        }
    }
}
</pre>
